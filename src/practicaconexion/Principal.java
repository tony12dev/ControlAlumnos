    private void TablaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaUsuariosMouseClicked
        // TODO add your handling code here:
        dt.setIdusuario(Integer.parseInt(TablaUsuarios.getValueAt(TablaUsuarios.getSelectedRow(),0).toString()));
        dt.setNombre(TablaUsuarios.getValueAt(TablaUsuarios.getSelectedRow(), 1).toString());
        dt.setUsuario(TablaUsuarios.getValueAt(TablaUsuarios.getSelectedRow(), 2).toString());
        dt.setContrase単a(TablaUsuarios.getValueAt(TablaUsuarios.getSelectedRow(), 3).toString());
        
        if(TablaUsuarios.getSelectedColumn()==4){
          if(JOptionPane.showConfirmDialog(this, "Desea Actualizar?", "Actualizar", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
           
              try {
                  del.actualizarUsuario(dt);
                  JOptionPane.showMessageDialog(null, "Datos Actualizados");
              } catch (Exception e) {
                  System.out.println("Algo esta Mal hay chequele");  
              }
              
          }else {
          
   this.dispose();
       new Principal().setVisible(true);
          } 
          
        }
        
        if(TablaUsuarios.getSelectedColumn()==5){
          if(JOptionPane.showConfirmDialog(this, "Desea Eliminar el registo?", "Eliminar", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
           
              try {
                  del.eliminarUsuario(dt);
                  JOptionPane.showMessageDialog(null, "Registro eliminado");
                  llenado();
              } catch (Exception e) {
                  System.out.println("Algo esta Mal hay chequele");  
              }
              
          }else {
          this.dispose();
       new Principal().setVisible(true);
          } 
          
        }
    }//GEN-LAST:event_TablaUsuariosMouseClicked

