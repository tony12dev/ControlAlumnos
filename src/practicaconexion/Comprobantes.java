    private void tablaComprobantesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaComprobantesMouseClicked
        // TODO add your handling code here:
        dt.setIdcomprobantes(Integer.parseInt(tablaComprobantes.getValueAt(tablaComprobantes.getSelectedRow(),0).toString()));
        dt.setnInicio(tablaComprobantes.getValueAt(tablaComprobantes.getSelectedRow(),1).toString());
        dt.setnFin(tablaComprobantes.getValueAt(tablaComprobantes.getSelectedRow(), 2).toString());
        dt.setSerie(tablaComprobantes.getValueAt(tablaComprobantes.getSelectedRow(), 3).toString());
        dt.setTipoComprobante(tablaComprobantes.getValueAt(tablaComprobantes.getSelectedRow(), 4).toString());
        
        if(tablaComprobantes.getSelectedColumn()==5){
          if(JOptionPane.showConfirmDialog(this, "Desea Actualizar?", "Actualizar", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
           
              try {
                  del.ActualizarComprobantes(dt);
                  JOptionPane.showMessageDialog(null, "Datos Actualizados");
              } catch (Exception e) {
                  System.out.println("Algo esta Mal hay chequele");  
              }
              
          }else {
          
   this.dispose();
       new Comprobantes().setVisible(true);
          } 
          
        }
        
        if(tablaComprobantes.getSelectedColumn()==6){
          if(JOptionPane.showConfirmDialog(this, "Desea Eliminar el registo?", "Eliminar", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
           
              try {
                  del.EliminarComprobantes(dt);
                  JOptionPane.showMessageDialog(null, "Registro eliminado");
                  llenadoComprobantes();
              } catch (Exception e) {
                  System.out.println("Algo esta Mal hay chequele");  
              }
              
          }else {
          this.dispose();
       new Comprobantes().setVisible(true);
          } 
          
        }
               
    }//GEN-LAST:event_tablaComprobantesMouseClicked

