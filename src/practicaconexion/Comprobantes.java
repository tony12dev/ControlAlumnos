    public void llenadoComprobantes() {

        String[] columnas = {"IdComprobantes", "ninicio", "nfin", "serie", "Tipo de Comprobante", "Modificar", "Eliminar"};

        List lista;
        int fila = -1;
        try {
            Object[][] data = new Object[del.lsComprobante().size()][7];
            lista = del.lsComprobante();
            Iterator it = lista.iterator();
            while (it.hasNext()) {
                dt = (ComprobantesDT) it.next();
                fila++;
                data[fila][0] = dt.getIdcomprobantes();
                data[fila][1] = dt.getnInicio();
                data[fila][2] = dt.getnFin();
                data[fila][3] = dt.getSerie();
                data[fila][4] = dt.getTipoComprobante();
                data[fila][5] = "Modificar";
                data[fila][6] = "Eliminar";
             

            }
            tablaComprobantes.setModel(new javax.swing.table.DefaultTableModel(data, columnas));
            System.out.println("SE HA llenado bien");
        } catch (Exception e) {
            System.out.println("Algo esta mal");

        }
    }

